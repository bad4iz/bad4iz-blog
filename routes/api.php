Route::middleware('auth:api')->get('/user', function (Request $request) {
    return $request->user();
});

Route::get('/get-github-file/{repository}/{fileName}', function (Request $request, $repository, $fileName) {
    $url ='https://raw.githubusercontent.com/bad4iz/'. $repository .'/master/'. $fileName;

    file_get_contents( $url);
    $ch = curl_init($url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
    curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
    curl_setopt($ch, CURLOPT_HEADER, false);
    $html = curl_exec($ch);
    curl_close($ch);

    return  $html;
});
